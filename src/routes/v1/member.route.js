const express = require('express');
const auth = require('../../middlewares/auth');
const memberController = require('../../controllers/member.controller');

const router = express.Router();

router.route('/').post(auth(), memberController.createMember).get(auth(), memberController.getMembers);

router
  .route('/:memberId')
  .get(auth(), memberController.getMember)
  .patch(auth(), memberController.updateMember)
  .delete(auth(), memberController.deleteMember);

module.exports = router;

